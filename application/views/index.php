<script>
    $("#formsubmit").submit(function (e) {
        e.preventDefault();
        var url = $(this).attr('action');
        var data = $(this).serialize();
        $.ajax({
            url: url,
            type: 'POST',
            data: data
        }).done(function (data) {
            $("#response").html(data);
            $("#formsubmit")[0].reset();
        });
    });
</script>

<script>
    var jump=function(e)
    {
        if (e){
            e.preventDefault();
            var target = $(this).attr("href");
        }else{
            var target = location.hash;
        }

        $('html,body').animate(
            {
                scrollTop: $(target).offset().top-89
            },1250,function()
            {
                location.hash = target;
            });

    }

    $('html, body').hide();

    $(document).ready(function()
    {
        $('a[href^=#]').bind("click", jump);

        if (location.hash){
            setTimeout(function(){
                $('html, body').scrollTop(0).show();
                jump();
            }, 0);
        }else{
            $('html, body').show();
        }
    });

</script>

